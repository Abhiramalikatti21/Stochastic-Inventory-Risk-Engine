def create_test_data(tmp_dir: Path) -> pd.DataFrame:
    """
    Generate synthetic M5 data, run Phase 1 pipeline, return the
    full featured DataFrame ready for Phase 2.
    """
    raw_dir = tmp_dir / "raw"
    processed_dir = tmp_dir / "processed"

    # Generate synthetic CSVs (5 items x 2 stores x 150 days)
    generate_synthetic_m5(raw_dir, n_items=5, n_days=150)

    # Run Phase 1
    config = PipelineConfig(
        data_dir=raw_dir,
        output_dir=processed_dir,
        warmup_days=30,
    )
    pipeline = DemandForecastingPipeline(config)
    pipeline.run(save=True)

    # Load the full featured dataset
    df = pd.read_parquet(processed_dir / "full_featured.parquet")
    print(f"[OK] Test data generated: {df.shape[0]:,} rows x {df.shape[1]} cols")
    return df


